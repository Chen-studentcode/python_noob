# =============================================================================
# enumerate(sequence, [start=0])  enumerate(序列(疊代器),預設計算起始為0 ) 
# 常用於循環中了解執行次數
# =============================================================================

list_a = ('one', 'two', 'three')
for i,x in enumerate(list_a,1):
    print(f'第{i}個元素為:{x}')
'''  
第1個元素為:one
第2個元素為:two
第3個元素為:three
'''
    